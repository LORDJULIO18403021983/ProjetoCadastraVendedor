package com.br.aulasOrbit;

public class IncrementoDecremento {
    public static void main(String[] args) {

        int numero = 1;
        System.out.println(" Pré-incremento " + ++numero);// pré-incremento
        numero = 1;
        System.out.println(" Pós-incremento " + numero++);// pós-incremento
    }
}
