package com.br.aulasOrbit;

public class Operacoes {
    public static void main(String[] args) {
        int adicao = 5 + 2;
        int subtracao = 5 - 2;
        int multiplicacao = 5 * 2;
        int divisao = 10 / 2;
        int modulo = 10 % 3;
        double peso = 90.35;
        boolean disponivel = true;
        char letra = 'a';
        String titulo = "Titanic";
        System.out.println("5 + 2 = " + adicao);
        System.out.println("5 - 2 = " + subtracao);
        System.out.println("5 * 2 = " + multiplicacao);
        System.out.println("10 / 2 = " + divisao);
        System.out.println("10 % 3 = " + modulo);
        System.out.println("Peso = " + peso);
        System.out.println("Disponivel = " + disponivel);
        System.out.println("Char = " + letra);
        System.out.println("String = " + titulo);
    }
}
